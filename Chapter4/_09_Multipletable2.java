package chap4;

public class _09_Multipletable2 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            for (int j = 1; j < 10; j++) {
                System.out.println(i+"*"+j+"="+(i*j));

            }
            System.out.println();

        }
    }
}

//반복문 for을 통한 구구단을 손쉽게 만들기


